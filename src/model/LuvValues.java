package model;

public class LuvValues {

	double valueL, valueU, valueV;
	
	public LuvValues(double valueL, double valueU, double valueV){
		this.valueL = valueL;
		this.valueU = valueU;
		this.valueV = valueV;
	}

	public double getValueL() {
		return valueL;
	}

	public double getValueU() {
		return valueU;
	}

	public double getValueV() {
		return valueV;
	}
	
}
